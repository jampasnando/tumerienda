<h2>1. Introducción</h2>

<p>
Bienvenido a <strong>TUMERIENDA</strong>.
</p>

<p>
La presente Política de Privacidad describe cómo recopilamos, utilizamos, almacenamos y protegemos la información personal de los usuarios que utilizan nuestra aplicación móvil TUMERIENDA y nuestros servicios asociados.
</p>

<p>
Al utilizar la aplicación, usted acepta las prácticas descritas en esta Política de Privacidad.
</p>

<h2>2. Información que recopilamos</h2>

<p>Para brindar nuestros servicios, podemos recopilar la siguiente información:</p>

<h3>Información del padre, madre o tutor</h3>

<ul>
<li>Nombre completo.</li>
<li>Número de documento de identidad (cuando sea requerido).</li>
<li>Número de teléfono.</li>
<li>Correo electrónico.</li>
<li>Dirección (si corresponde).</li>
<li>Información necesaria para administrar la cuenta.</li>
</ul>

<h3>Información de los beneficiarios</h3>

<p>
Para administrar correctamente la entrega de meriendas en las unidades educativas, recopilamos información de los estudiantes registrados por sus padres o tutores, como:
</p>

<ul>
<li>Nombre completo.</li>
<li>Curso o grado.</li>
<li>Unidad educativa.</li>
<li>Datos necesarios para identificar al beneficiario durante la entrega de la merienda.</li>
</ul>

<h3>Información de uso</h3>

<p>La aplicación puede recopilar automáticamente información técnica como:</p>

<ul>
<li>Modelo del dispositivo.</li>
<li>Sistema operativo.</li>
<li>Dirección IP.</li>
<li>Registros de uso de la aplicación.</li>
<li>Información relacionada con errores para mejorar el servicio.</li>
</ul>

<h2>3. Cómo utilizamos la información</h2>

<p>La información recopilada es utilizada para:</p>

<ul>
<li>Registrar a los padres y beneficiarios.</li>
<li>Gestionar las suscripciones a los diferentes planes de merienda.</li>
<li>Coordinar y controlar la entrega de meriendas en las unidades educativas.</li>
<li>Brindar soporte técnico.</li>
<li>Enviar notificaciones relacionadas con el servicio.</li>
<li>Mejorar el funcionamiento de la aplicación.</li>
<li>Cumplir obligaciones legales cuando corresponda.</li>
</ul>

<h2>4. Compartición de información</h2>

<p>
No vendemos ni alquilamos la información personal de nuestros usuarios.
</p>

<p>
Podremos compartir información únicamente cuando sea necesario para:
</p>

<ul>
<li>Operar correctamente el servicio.</li>
<li>Coordinar la entrega de meriendas con las unidades educativas correspondientes.</li>
<li>Cumplir obligaciones legales o requerimientos de autoridades competentes.</li>
<li>Proteger los derechos y la seguridad de nuestros usuarios o de TUMERIENDA.</li>
</ul>

<h2>5. Seguridad de la información</h2>

<p>
Implementamos medidas técnicas y administrativas razonables para proteger la información personal contra accesos no autorizados, pérdida, alteración o divulgación.
</p>

<p>
Sin embargo, ningún sistema de transmisión o almacenamiento electrónico es completamente seguro, por lo que no podemos garantizar una seguridad absoluta.
</p>

<h2>6. Conservación de los datos</h2>

<p>
Conservaremos la información personal únicamente durante el tiempo necesario para prestar nuestros servicios, cumplir obligaciones legales o resolver controversias.
</p>

<h2>7. Derechos de los usuarios</h2>

<p>Los usuarios pueden solicitar:</p>

<ul>
<li>Acceder a su información personal.</li>
<li>Corregir información inexacta.</li>
<li>Actualizar sus datos.</li>
<li>Solicitar la eliminación de su cuenta cuando sea legalmente posible.</li>
<li>Solicitar información sobre el tratamiento de sus datos personales.</li>
</ul>

<h2>8. Privacidad de menores</h2>

<p>
La aplicación está destinada a ser utilizada por padres, madres o tutores.
</p>

<p>
La información de los estudiantes beneficiarios es proporcionada y administrada únicamente por sus respectivos padres, madres o tutores con el fin exclusivo de gestionar el servicio de entrega de meriendas.
</p>

<h2>9. Servicios de terceros</h2>

<p>
La aplicación puede utilizar servicios de terceros para mejorar su funcionamiento, tales como servicios de autenticación, almacenamiento en la nube, notificaciones o análisis de funcionamiento.
</p>

<p>
Estos proveedores podrán tratar cierta información conforme a sus propias políticas de privacidad.
</p>

<h2>10. Cambios a esta Política de Privacidad</h2>

<p>
Podremos actualizar esta Política de Privacidad en cualquier momento.
</p>

<p>
Las modificaciones serán publicadas en nuestro sitio web y entrarán en vigencia desde su publicación.
